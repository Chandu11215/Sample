public class Shift {
    public static void main(String[] args) {
        int a = 2;
        int b = -2;
        System.out.println("a << 2 = : " + (a<<2));
        System.out.println("a >> 2 = : " + (a>>2));
        System.out.println("b << 24= : " + (b<<24));
        System.out.println("b >> 24 = : " + (b>>24));
    }
}
